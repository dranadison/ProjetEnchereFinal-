public class Erreur {
    String code,message;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Erreur(String code, String message) {
        this.code = code;
        this.message = message;
    }
    
    
    
}
